package ru.netology;

import java.math.BigInteger;

public class Fibonacci {
    public static BigInteger get(int n) {
        BigInteger a = BigInteger.ZERO;
        BigInteger b = BigInteger.ONE;
        for (int i = 0; i < n; i++) {
            BigInteger c = a.add(b);
            a = b;
            b = c;
        }
        return a;
    }
}
